using System;
using System.Collections.Concurrent;
using System.Collections.Generic;
using System.Text;
using System.Threading.Tasks;
using System.Linq;
using Statistics;

namespace Model
{
    /// <summary>
    /// Provides an interface for fully computed objects implementing the <see cref="IConditionLocationYear{T}"/> interface.
    /// </summary>
    public interface IConditionLocationYearResult
    {
        void Compute();
        /// <summary>
        /// A random seed used to generate <see cref="IConditionLocationYearRealization"/>s during the <see cref="Conditions.Locations.Years.Results.ConditionLocationYearResult.Compute(IReadOnlyDictionary{IMetric, IConvergenceCriteria})"/> that is called upon construction of the <see cref="IConditionLocationYearResult"/> object.
        /// </summary>
        int Seed { get; }
        /// <summary>
        /// The object implementing the <see cref="IConditionLocationYearSummary"/> interface used to generate the <see cref="IConditionLocationYearResult"/>.
        /// </summary>
        IConditionLocationYearSummary ConditionLocationTime { get; }
        /// <summary>
        /// The <see cref="IConditionLocationYearRealizationSummary"/>s that comprise the <see cref="IConditionLocationYearResult"/>. These are generated by the <see cref="Conditions.Locations.Years.Results.ConditionLocationYearResult.Compute(IReadOnlyDictionary{IMetric, IConvergenceCriteria})"/> method that is called during construction of the object implementing the <see cref="IConditionLocationYearResult"/> interface.
        /// </summary>
        IReadOnlyList<IConditionLocationYearRealizationSummary> Realizations { get; }
        /// <summary>
        /// A <see cref="IReadOnlyDictionary{TKey, TValue}"/> of <see cref="IMetric"/> keys and <see cref="Statistics.IHistogram"/> values describing the distribution of <see cref="IMetric.Compute(IFrequencyFunction, double)"/> values. These values are generated the <see cref="IConditionLocationYearRealizationSummary.Metrics"/> created during the <see cref="Conditions.Locations.Years.Results.ConditionLocationYearResult.Compute(IReadOnlyDictionary{IMetric, IConvergenceCriteria})"/> method called during construction of the object implementing the <see cref="IConditionLocationYearResult"/> interface.
        /// </summary>
        IReadOnlyDictionary<IMetric, Statistics.Histograms.Histogram> Metrics { get; set; }
        /// <summary>
        /// A <see cref="IReadOnlyDictionary{TKey, TValue}"/> of <see cref="IMetric"/> keys and <see cref="Statistics.IConvergenceResult"/> values describing convergence of the <see cref="Metrics"/> values.
        /// </summary>
        IReadOnlyDictionary<IMetric, Statistics.IConvergenceResult> Convergence { get; }
    }

    
}
